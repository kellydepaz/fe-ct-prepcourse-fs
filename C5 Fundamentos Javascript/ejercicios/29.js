function obtenerAreaRectangulo(alto, ancho) {
  // Retornar el área de un rectángulo teniendo su alto y ancho.
  // Tu código:
  let resultadoAreaDeUnRectangulo = alto * ancho;
  return resultadoAreaDeUnRectangulo;

}

